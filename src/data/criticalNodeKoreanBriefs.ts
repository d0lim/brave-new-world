export function localizeEconInsightBrief(
  brief: EconInsightBrief,
  lang: LabelLanguage,
): EconInsightBrief {
  if (lang !== "ko" || !brief.criticalNodeId) return brief;
  const ko = KO_CRITICAL_NODE_BRIEFS[brief.criticalNodeId];
  if (!ko) return brief;
  return {
    ...brief,
    titleKo: ko.title ?? brief.titleKo,
    impactLine: ko.impactLine,
    paragraphs: [...ko.paragraphs, ...brief.paragraphs.filter((p) => !/[A-Za-z]{4,}/.test(p))],
  };
}
